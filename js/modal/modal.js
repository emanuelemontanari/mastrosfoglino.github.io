// Modal-sfoglina-o
$('#sfoglina-o-modal').change(function() {
      if ($(this).is(":checked")) {
        $('body').addClass('modal-open');
      } else {
        $('body').removeClass('modal-open');
      }
    });
    $('#sfoglina-o-modal').change(function() {
      if ($(this).is(":checked")) {
        $('.modal-sfoglina-o').addClass('in');
      } else {
        $('.modal-sfoglina-o').removeClass('in');
      }
    });
    $('#sfoglina-o-modal').change(function() {
      if ($(this).is(":checked")) {
        $('.modal-back').addClass('modal-backdrop');
      } else {
        $('.modal-back').removeClass('modal-backdrop');
      }
    });
    
